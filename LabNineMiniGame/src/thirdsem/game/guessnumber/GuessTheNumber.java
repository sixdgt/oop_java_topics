    /**
     * @return generated_number
     */
    public int generateNumber(){
        // Using Random package to generate number
        Random random = new Random();
        int generated_number = random.nextInt(1, 13);
        return generated_number;
    }
    
    /**
     * @param number
     * 
     */
    public void checkGuessedNumber(int number){
        int generated_number = this.generateNumber();
        if(number == generated_number){
            text_result.setText("Guess number is correct");
            setButtonColor(generated_number, Color.GREEN);
            JOptionPane.showMessageDialog(rootPane, "Match found after trying " + this.count + " times");
            text_number.setText("");
            this.clearButtonColor();
            this.count = 0;
            text_count.setText(this.count + "");
        } else {
            text_result.setText("Guess failed. The number is: " + generated_number);
            setButtonColor(generated_number, Color.RED);
            this.count++;
            text_count.setText(this.count + "");
        }
    }
    
