    private void btn_guessActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guessActionPerformed
        // TODO add your handling code here:
        String guess_number = text_number.getText();
        if(guess_number.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Please enter number first");
        } else {
            try {
                int number = Integer.parseInt(guess_number);
                this.clearButtonColor();
                // checking the number
                this.checkGuessedNumber(number);
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(rootPane, "Must be numeric value");
            }
        }
    }//GEN-LAST:event_btn_guessActionPerformed

